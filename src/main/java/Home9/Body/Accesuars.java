package Home9.Body;

public class Accesuars {
    public int price;
    public String name;

    public Accesuars() {
        this.price = price;
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String toString() {
        return "Name of Accesuar " + name + "his price" + price;
    }
}
